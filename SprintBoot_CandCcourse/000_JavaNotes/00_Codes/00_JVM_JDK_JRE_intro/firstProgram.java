public class firstProgram{
//    main  -->  starting point of any Java program (method)
//    void  -->  return type of method
//    static  -->  Means we don't need object of the class to call any method of it. We can directly access any method of class by the class itself.
//    public  -->  Access specifier (private, protected, public).
//    public-method  =>  this method can be called from anywhere.
    public static void main(String args[]) {
        int a = -10;
        System.out.println("a = " + a);
    }
}